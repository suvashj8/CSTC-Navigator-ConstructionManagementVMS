export const NEPAL_RTA_OFFICES_FLAT: readonly string[] = Object.values(NEPAL_RTA_BY_PROVINCE).flat();

export function filterRtaOfficesByQuery(query: string, limit = 18): string[] {
  const q = query.trim().toLowerCase();
  if (!q) return [];

  type Ranked = { office: string; rank: number };
  const ranked: Ranked[] = [];

  for (const office of NEPAL_RTA_OFFICES_FLAT) {
    const lower = office.toLowerCase();
    if (lower.startsWith(q)) {
      ranked.push({ office, rank: 0 });
      continue;
    }
    const words = lower.split(/[\s/(),—\-]+/).filter(Boolean);
    if (words.some((w) => w.startsWith(q))) {
      ranked.push({ office, rank: 1 });
      continue;
    }
    if (lower.includes(q)) {
      ranked.push({ office, rank: 2 });
    }
  }

  ranked.sort(
    (a, b) => a.rank - b.rank || a.office.localeCompare(b.office, "en", { sensitivity: "base" })
  );
  return ranked.slice(0, limit).map((r) => r.office);
}

/** Filter string options: prefix match first, then word prefix, then contains. */
export function filterOptionsByQuery(options: readonly string[], query: string, limit = 12): string[] {
  const q = query.trim().toLowerCase();
  if (!q) return [...options].slice(0, limit);

  type Ranked = { value: string; rank: number };
  const ranked: Ranked[] = [];

  for (const opt of options) {
    const lower = opt.toLowerCase();
    if (lower.startsWith(q)) {
      ranked.push({ value: opt, rank: 0 });
      continue;
    }
    if (lower.split(/\s+/).some((w) => w.startsWith(q))) {
      ranked.push({ value: opt, rank: 1 });
      continue;
    }
    if (lower.includes(q)) {
      ranked.push({ value: opt, rank: 2 });
    }
  }

  ranked.sort(
    (a, b) => a.rank - b.rank || a.value.localeCompare(b.value, "en", { sensitivity: "base" })
  );
  return ranked.slice(0, limit).map((r) => r.value);
}

